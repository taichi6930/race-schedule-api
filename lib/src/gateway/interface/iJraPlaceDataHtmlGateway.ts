/**
 * JRA（日本中央競馬会）の競馬場開催情報を取得するゲートウェイインターフェース
 *
 * このインターフェースは、JRAの公式Webサイトから競馬場の開催スケジュール
 * 情報をHTMLとして取得する機能を提供します。主な責務：
 * - 指定された月の開催カレンダーページにアクセス
 * - 競馬場ごとの開催日程情報の取得
 * - HTMLコンテンツの適切な取得
 *
 * 特徴：
 * - 月単位でのスケジュール情報取得
 * - 全競馬場の開催情報を一括取得
 * - 開催回数や開催日数の情報を含む
 *
 * 注意事項：
 * - 通常、2-3ヶ月先までの情報のみ公開されます
 * - 祝日や特別日程での開催変更の可能性があります
 * - HTMLの構造変更に影響を受けやすい実装です
 */
export interface IJraPlaceDataHtmlGateway {
    /**
     * 指定された月の競馬場開催情報のHTMLを取得します
     *
     * このメソッドは以下の処理を行います：
     * 1. 指定された年月のカレンダーURLを構築
     * 2. JRAサイトにアクセス
     * 3. 開催情報を含むHTMLを取得
     *
     * 取得されるHTMLには以下の情報が含まれます：
     * - 各競馬場の開催スケジュール
     * - 開催回数と開催日数
     * - GIレースなどの重要な開催情報
     * - 特別な開催に関する注記
     *
     * @param date - 取得対象の年月を表すDate型
     *               日付部分は無視され、年月のみが使用されます
     * @returns 競馬場開催情報のHTML文字列
     * @throws Error 以下の場合にエラーが発生：
     *               - ネットワーク接続エラー
     *               - サイトがメンテナンス中
     *               - アクセス制限による拒否
     *               - 予期しないページ構造の変更
     *               - 指定された月の情報が未公開
     */
    getPlaceDataHtml: (date: Date) => Promise<string>;
}
