/**
 * JRA（日本中央競馬会）のWebサイトからレース情報を取得するゲートウェイインターフェース
 *
 * このインターフェースは、JRAの公式Webサイトから特定日のレース開催情報を
 * HTMLとして取得する機能を提供します。主な責務：
 * - 指定された日付のレース情報ページにアクセス
 * - HTMLコンテンツの取得
 * - 適切なエラーハンドリング
 *
 * 特徴：
 * - HTMLスクレイピングベースのデータ取得
 * - アクセス頻度の制御
 * - ページの構造変更への対応
 *
 * 注意事項：
 * - JRAサイトのアクセス制限を考慮する必要があります
 * - HTMLの構造変更に影響を受けやすい実装です
 * - 大量アクセスによるブロックを避ける必要があります
 */
export interface IJraRaceDataHtmlGateway {
    /**
     * 指定された日付のレース情報ページのHTMLを取得します
     *
     * このメソッドは以下の処理を行います：
     * 1. 指定された日付のURLを構築
     * 2. JRAサイトにアクセス
     * 3. HTMLコンテンツを取得
     *
     * 取得されるHTMLには以下の情報が含まれます：
     * - その日に開催される全レースの一覧
     * - 各レースの基本情報（レース名、時刻、場所など）
     * - レースの詳細情報へのリンク
     *
     * @param date - 取得対象の日付
     * @returns レース情報ページのHTML文字列
     * @throws Error 以下の場合にエラーが発生：
     *               - ネットワーク接続エラー
     *               - サイトがメンテナンス中
     *               - アクセス制限による拒否
     *               - 予期しないページ構造の変更
     */
    getRaceDataHtml: (date: Date) => Promise<string>;
}
